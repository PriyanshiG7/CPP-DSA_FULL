// bitwise operator 
#include<iostream>
using namespace std;
int main(){
    int a = 4;
    int b=6;
     cout<<" a&b "<< (a&b) <<endl;  //and op.
     cout<<" a|b "<< (a|b) <<endl;  //or op.
     cout<<" ~a "<< ~a <<endl;      //not op.
     cout<<" a^b "<< (a^b) <<endl;  //xor op.
     }