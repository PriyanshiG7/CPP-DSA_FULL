 // shift op.(<<)
 #include<iostream>
 using namespace std;
    //left shift op.(<<)
 int main (){
    int a=5;
    int b=1;
    cout <<(a<<b) <<endl; 
    int c=0100000010;
    cout <<(c<<1)<<endl;;
    // right shift op.(>>)
    cout<<(a>>b)<<endl; //(5*1+5=10)
    cout <<(a<<2); //(5*2+5+5 =20)
    }