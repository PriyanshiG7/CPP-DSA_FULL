//switch case:
#include <iostream>
using namespace std;
int main(){
    char ch ='1';
    cout << endl;
    switch (ch){
        case 1: cout<<"first"<<endl;
        break;
        case '1': cout <<"character one"<<endl;
        break;
        default: cout<<"its default case"<<endl;
    
    }
    cout<<endl;    
}